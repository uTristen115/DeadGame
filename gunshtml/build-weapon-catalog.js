const fs = require('fs');
const path = require('path');

const directory = __dirname;
const knownWeaponIds = {
  '1911handgunmodel.json': 'pistol',
  'minismg.json': 'smg',
  'm4riflemodel.json': 'rifle',
  'semiautorifle.json': 'carbine'
};

function displayName(filename) {
  return path.basename(filename, path.extname(filename))
    .replace(/[_-]+/g, ' ')
    .replace(/([a-z])([A-Z])/g, '$1 $2')
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

function catalogId(filename) {
  return `forge_${path.basename(filename, path.extname(filename))
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')}`;
}

const files = fs.readdirSync(directory)
  .filter((filename) => filename.toLowerCase().endsWith('.json'))
  .sort((a, b) => a.localeCompare(b));

const modelsByFile = {};
const sourceFiles = [];

for (const filename of files) {
  const project = JSON.parse(fs.readFileSync(path.join(directory, filename), 'utf8'));
  if (!project || !project.object) continue;
  const userData = project.object.userData || {};
  const embeddedId = userData.forgeWeaponId && userData.forgeWeaponId !== 'custom'
    ? userData.forgeWeaponId
    : null;
  modelsByFile[filename] = project;
  sourceFiles.push({
    filename,
    catalogId: catalogId(filename),
    name: userData.forgeProjectName || displayName(filename),
    weaponId: embeddedId || knownWeaponIds[filename.toLowerCase()] || null,
    gameReady: userData.forgeGameReady === true
  });
}

const output = [
  '/* Generated by build-weapon-catalog.js. Rebuild after adding or replacing a source weapon JSON file. */',
  `window.DEAD_GAME_SOURCE_WEAPON_FILES=${JSON.stringify(sourceFiles)};`,
  `window.DEAD_GAME_WEAPON_MODELS_BY_FILE=${JSON.stringify(modelsByFile)};`,
  ''
].join('\n');

fs.writeFileSync(path.join(directory, 'weapon-model-catalog.js'), output);
console.log(`Bundled ${sourceFiles.length} weapon model${sourceFiles.length === 1 ? '' : 's'}.`);
